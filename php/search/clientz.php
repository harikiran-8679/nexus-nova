        /*Modal Javascript!!*/
        document.addEventListener('DOMContentLoaded', function() {
            let modal = document.getElementById("modal");
            let span = document.getElementsByClassName("close")[0];
        
            document.querySelectorAll('.cards').forEach(card => {
                card.addEventListener('click', function() {
                    const imageSrc = this.querySelector('.card-image').src;
                    const name = this.querySelector('.card-name')?.textContent || 'Name not available';
                    const company = this.querySelector('.card-company')?.textContent || 'Company not available';
                    let email = this.querySelector('.card-email')?.textContent.trim() || ''; // Trim any whitespace

                    console.log("Original email:", email);

                    email = email.replace(/[;]+/g, '');

                    console.log("Cleaned email:", email);

                    const skill = this.querySelector('.card-skill')?.textContent || 'Skill not available';
                    const description = this.querySelector('.card-description')?.textContent || 'Description not available';
        
                    document.getElementById('modal-image').src = imageSrc;
                    document.getElementById('modal-name').textContent = name;
                    document.getElementById('modal-email').textContent = email;
                    document.getElementById('modal-company').textContent = company;
                    document.getElementById('modal-skill').textContent = skill;
                    document.getElementById('modal-description').textContent = description;
        
                    if (email && email.includes('@')) {
                        document.getElementById("request-link").href = `mailto:${email}?subject=Request%20for%20Freelance%20Work&body=Hello%20${name},%0AI%20am%20interested%20in%20discussing%20a%20freelance%20opportunity%20with%20you.%0APlease%20get%20in%20touch%20with%20me.%0A%0ABest%20regards,%0A[Your%20Name]`;
                        document.getElementById("request-link").style.display = "block";
                    } else {
                        document.getElementById("request-link").style.display = "none"; // Hide the button if email is not available
                    }
        
                    modal.style.display = "block";
                });
            });
        
            // When the user clicks on <span> (x), close the modal
            span.onclick = function() {
                modal.style.display = "none";
            }
        
            // When the user clicks anywhere outside of the modal, close it
            window.onclick = function(event) {
                if (event.target == modal) {
                    modal.style.display = "none";
                }
            }
        });
        


    </script>

